<script>
    $(document).on('submit', '#contactForm', function(event) {
        event.preventDefault(); // Ngừng hành động mặc định của form

        var name = $("#name").val();
        var phone = $("#phone").val();
        var email = $("#email").val();
        var subject = $("#subject").val();
        var message = $("#textarea").val();

        // Kiểm tra nếu có trường thông tin bị trống
        if (!name || !phone || !email || !subject || !message) {
            toast({
                title: "Lỗi",
                message: "Vui lòng điền đầy đủ thông tin.",
                type: "error",
                duration: 5000
            });
            return;
        }

        $.ajax({
            url: '<?php echo _WEB_ROOT_; ?>/contact_us/sendContactForm', // Đường dẫn tới controller xử lý
            type: 'POST',
            data: {
                name: name,
                phone: phone,
                email: email,
                subject: subject,
                message: message
            },
            dataType: 'json'
        })
        .done(function(response) {
            if (response.success) {
                toast({
                    title: "Thành công",
                    message: "Ý kiến của bạn đã được gửi thành công. Chúng tôi sẽ phản hồi sớm nhất có thể.",
                    type: "success",
                    duration: 5000
                });
            } else {
                var errorMessage = response.message || "Có lỗi xảy ra. Vui lòng thử lại.";
                toast({
                    title: "Lỗi",
                    message: errorMessage,
                    type: "error",
                    duration: 5000
                });
            }
        })
        .fail(function(xhr, status, error) {
            console.error("Status:", status);
            console.error("Error:", error);
            console.error("Response:", xhr.responseText);
            toast({
                title: "Lỗi hệ thống",
                message: "Có lỗi xảy ra khi xử lý yêu cầu. Vui lòng thử lại.",
                type: "error",
                duration: 5000
            });
        });
    });

</script>


